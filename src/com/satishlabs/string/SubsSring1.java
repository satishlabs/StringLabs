package com.satishlabs.string;

public class SubsSring1 {
	public static void main(String[] args) {
		String str="abc";
		System.out.println(str.substring(1));
		System.out.println(str.substring(0));
		char ch = str.charAt(0);
		System.out.println(ch);
		ch = str.charAt(3);
		System.out.println(ch);
	}
}
